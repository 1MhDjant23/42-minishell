#include "../minishell.h"

void	close_pipe(int i, int **pip)
{
	if (i > 0)
	{
		close(pip[i - 1][0]);
		close(pip[i - 1][1]);
	}
}

void	free_pipe(int **pipe, int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		free(pipe[i]);
		pipe[i] = NULL;
		i++;
	}
	free(pipe);
	pipe = NULL;
}
