#include "../minishell.h"

void	built_env(t_minishel *data)
{
	t_env	*ev;

	ev = data->env;
	while (ev)
	{
		if (ev && ev->value && ev->name)
			printf("%s=%s\n", ev->name, ev->value);
		ev = ev->next;
	}
	data->exit_status = 0;
}

char	*get_env(t_minishel *data, char *var)
{
	t_env	*ev;

	ev = data->env;
	if (!ev)
		return (NULL);
	while (ev)
	{
		if (ft_strcmp(var, ev->name) == 0)
			return (ft_strdup(ev->value));
		ev = ev->next;
	}
	return (NULL);
}
